package com.shpach.board;
import static java.lang.Math.*;
public class Point {
	private double x;
	private double y;

	public Point(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}

	public Point() {
		super();
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "Point [x=" + x + ", y=" + y + "]";
	}

	public static double getLength(Point x2, Point y2) {
		return sqrt(pow((x2.x-y2.x),2)+pow((x2.y-y2.y),2));
	}

}
